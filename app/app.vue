function toggleDarkMode() {
  console.log('=== Theme Toggle Started ===')
  console.log('Current isDarkMode value:', isDarkMode.value)
  console.log('Current document classes:', document.documentElement.className)

  // Toggle the theme
  isDarkMode.value = !isDarkMode.value

  console.log('New isDarkMode value:', isDarkMode.value)
  console.log('Toggling to:', isDarkMode.value ? 'DARK' : 'LIGHT', 'mode')

  // Save the new preference
  localStorage.setItem('darkMode', isDarkMode.value.toString())
  console.log('Saved to localStorage:', localStorage.getItem('darkMode'))

