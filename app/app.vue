  console.log('=== Updating DOM Classes ===')
  console.log('isDarkMode.value:', isDarkMode.value)
  console.log('Document classes before update:', document.documentElement.className)

  // Force remove any existing dark class first
  document.documentElement.classList.remove('dark')

  if (isDarkMode.value) {
    console.log('Adding dark class to document.documentElement')
    document.documentElement.classList.add('dark')
  } else {
    console.log('Keeping document.documentElement in light mode (no dark class)')
  }

  console.log('Document classes after update:', document.documentElement.className)
  console.log('Has dark class:', document.documentElement.classList.contains('dark'))

  // Verify the change took effect
  setTimeout(() => {
    const hasDark = document.documentElement.classList.contains('dark')
    console.log('Verification - Document has dark class:', hasDark)
    console.log('Verification - Should match isDarkMode:', hasDark === isDarkMode.value)
  }, 100)
}

