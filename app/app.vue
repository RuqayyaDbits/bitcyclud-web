const fetchOnlineUsers = async () => {
  try {
    const response = await fetch('https://new-api.bitcy.club/graphql', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        query: `
          query OnlineUsers {
            onlineUsers {
              id
            }
          }
        `
      })
    })

    if (!response.ok) {
      throw new Error(`HTTP ${response.status}`)
    }

    const data = await response.json()
    if (data.data?.onlineUsers) {
      onlineUserCount.value = data.data.onlineUsers.length || 0
    }
  } catch (error) {
    console.error('Failed to fetch online users:', error)
  }
}

