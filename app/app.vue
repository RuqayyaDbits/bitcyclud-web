  console.log('savedMode from localStorage:', savedMode)

  if (savedMode !== null) {
    // User has a saved preference, use it
    isDarkMode.value = savedMode === 'true'
    console.log('Using saved user preference:', isDarkMode.value)
  } else {
    // No saved preference, default to dark mode
    isDarkMode.value = true
    console.log('No saved preference, defaulting to dark mode')
    // Save the default choice
    localStorage.setItem('darkMode', 'true')
